package marsrover.common;

import static org.apache.commons.lang3.builder.EqualsBuilder.reflectionEquals;
import static org.apache.commons.lang3.builder.HashCodeBuilder.reflectionHashCode;
import static org.apache.commons.lang3.builder.ToStringBuilder.reflectionToString;
import static org.apache.commons.lang3.builder.ToStringStyle.SHORT_PREFIX_STYLE;

public class IllegalRoverCommandException extends IllegalArgumentException {

    private final Character letter;

    public IllegalRoverCommandException(Character letter) {
        this.letter = letter;
    }

    @Override
    public String getMessage() {
        return String.format("Cannot parse instructions due to invalid command. command=%s", letter);
    }

    @Override
    public boolean equals(Object obj) {
        return reflectionEquals(this, obj);
    }

    @Override
    public int hashCode() {
        return reflectionHashCode(this);
    }

    @Override
    public String toString() {
        return reflectionToString(this, SHORT_PREFIX_STYLE);
    }
}
